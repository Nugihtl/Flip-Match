    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int barisTerpilih = jTable1.getSelectedRow();
        
        if (barisTerpilih >= 0) {
            // Ambil ID dari kolom pertama (indeks 0)
            int idLevel = (int) jTable1.getValueAt(barisTerpilih, 0);
            
            int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus level ini?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);
            
            if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
                if (levelDAO.delete(idLevel)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus.");
                    muatDataTabel(); // Segarkan tabel
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                }
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih baris pada tabel terlebih dahulu.", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnEditLevelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditLevelActionPerformed
        // TODO add your handling code here
        int barisTerpilih = jTable1.getSelectedRow();
        
        if (barisTerpilih >= 0) {
            java.util.List<Level> dataLevel = levelDAO.getAll();
            Level lvlTerpilih = dataLevel.get(barisTerpilih);
            
            FormLevelInput formInput = new FormLevelInput(this);
            formInput.setModeEdit(
                lvlTerpilih.getIdLevel(), 
                lvlTerpilih.getIdTheme(), 
                lvlTerpilih.getNamaLevel(), 
                lvlTerpilih.getBaris(), 
                lvlTerpilih.getKolom(), 
                lvlTerpilih.getWaktuDetik()
            );
            formInput.setVisible(true);
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Silakan pilih baris pada tabel terlebih dahulu untuk diedit.", "Peringatan", javax.swing.JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnEditLevelActionPerformed

